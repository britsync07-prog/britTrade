const TelegramBot = require('node-telegram-bot-api');
const db = require('../db');
const authService = require('./authService');

const token = process.env.TELEGRAM_TOKEN;
let bot;

// в”Ђв”Ђв”Ђ Helper: format a rich signal message в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
function formatEntryMessage(strategyName, signal) {
  const { symbol, side, price, tp, sl, stakeAmount } = signal;
  const isLong = side === 'buy' || side === 'long';
  const sideEmoji = isLong ? 'рџџў' : 'рџ”ґ';
  const sideLabel = isLong ? 'рџ“€ BUY (LONG)' : 'рџ“‰ SELL (SHORT)';

  return (
    `${sideEmoji} *NEW SIGNAL вЂ” ${strategyName}*\n` +
    `в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ\n` +
    `*Pair:*         \`${symbol}\`\n` +
    `*Direction:*    ${sideLabel}\n` +
    `*Entry Price:*  \`$${Number(price).toFixed(4)}\`\n` +
    `*Take Profit:*  \`$${Number(tp).toFixed(4)}\` рџЋЇ\n` +
    `*Stop Loss:*    \`$${Number(sl).toFixed(4)}\` рџ›‘\n` +
    `*Stake:*        \`$${stakeAmount || 100}\`\n` +
    `в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ\n` +
    `_Signal generated by BritSyncAI_`
  );
}

function formatExitMessage(strategyName, signal) {
  const { symbol, side, price, pnl } = signal;
  const isProfit = (pnl || 0) >= 0;
  const pnlEmoji = isProfit ? 'рџџў' : 'рџ”ґ';
  const pnlSign = isProfit ? '+' : '';
  const outcomeLabel = signal.status === 'tp_hit' ? 'рџЋЇ Take Profit Hit' : (signal.status === 'sl_hit' ? 'рџ›‘ Stop Loss Hit' : 'вњ… Trade Closed');

  return (
    `${pnlEmoji} *${outcomeLabel} вЂ” ${strategyName}*\n` +
    `в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ\n` +
    `*Pair:*         \`${symbol}\`\n` +
    `*Exit Side:*    ${side.toUpperCase()}\n` +
    `*Exit Price:*   \`$${Number(price).toFixed(4)}\`\n` +
    `*PnL:*          ${pnlEmoji} \`${pnlSign}${Number(pnl || 0).toFixed(2)}%\`\n` +
    `в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ\n` +
    `_BritSyncAI Trading Terminal_`
  );
}

// в”Ђв”Ђв”Ђ Bot Setup в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
if (token && token !== 'your_telegram_bot_token_here') {
  bot = new TelegramBot(token, { polling: { interval: 1000, autoStart: false, params: { timeout: 10 } } });
  bot.startPolling({ restart: false, dropPendingUpdates: true });
  console.log('[Telegram] Bot initialized: @BritSyncAI_bot');

  const sessions = {}; // tracks per-chat registration state

  // /start вЂ” greet & prompt login
  bot.onText(/\/start/, (msg) => {
    const chatId = msg.chat.id;
    sessions[chatId] = { step: 'email' };
    bot.sendMessage(chatId,
      `рџ¤– *Welcome to BritSyncAI Signal Bot!*\n\n` +
      `You will receive live trading signals for your purchased strategies.\n\n` +
      `Please enter your *account email* to link your account:`,
      { parse_mode: 'Markdown' }
    );
  });

  // /help вЂ” list commands
  bot.onText(/\/help/, (msg) => {
    bot.sendMessage(msg.chat.id,
      `рџ“‹ *Available Commands*\n\n` +
      `/start вЂ” Link your BritSyncAI account\n` +
      `/mystrats вЂ” Show your subscribed strategies\n` +
      `/logout вЂ” Unlink Telegram from your account\n` +
      `/help вЂ” Show this menu`,
      { parse_mode: 'Markdown' }
    );
  });

  // /mystrats вЂ” show which strategies user is subscribed to
  bot.onText(/\/mystrats/, async (msg) => {
    const chatId = msg.chat.id;
    const user = await db.get("SELECT id, email FROM users WHERE telegramId = ?", [chatId.toString()]);
    if (!user) {
      return bot.sendMessage(chatId, 'вќЊ You are not linked. Send /start to connect your account.');
    }

    const subs = await db.query(
      "SELECT s.name, s.type FROM strategies s JOIN subscriptions sub ON s.id = sub.strategyId WHERE sub.userId = ?",
      [user.id]
    );

    if (subs.length === 0) {
      return bot.sendMessage(chatId,
        `рџ“­ *No active strategies found.*\n\nPurchase a plan on the dashboard to receive signals.`,
        { parse_mode: 'Markdown' }
      );
    }

    const stratList = subs.map(s => `вЂў *${s.name}* (${s.type})`).join('\n');
    bot.sendMessage(chatId,
      `рџ“Љ *Your Active Signal Subscriptions:*\n\n${stratList}\n\n` +
      `_You will receive BUY/SELL signals for all of the above strategies._`,
      { parse_mode: 'Markdown' }
    );
  });

  // /logout вЂ” unlink account
  bot.onText(/\/logout/, async (msg) => {
    await db.run("UPDATE users SET telegramId = NULL WHERE telegramId = ?", [msg.chat.id.toString()]);
    bot.sendMessage(msg.chat.id, 'рџ‘‹ You have been unlinked from BritSyncAI signal alerts.');
  });

  // message handler вЂ” email/password login flow
  bot.on('message', async (msg) => {
    const chatId = msg.chat.id;
    const text = msg.text;
    const session = sessions[chatId];

    if (!session || !text || text.startsWith('/')) return;

    if (session.step === 'email') {
      session.email = text.trim();
      session.step = 'password';
      bot.sendMessage(chatId, 'рџ”‘ Got it! Now enter your *password*:', { parse_mode: 'Markdown' });
    } else if (session.step === 'password') {
      try {
        const { user } = await authService.login(session.email, text);
        await db.run("UPDATE users SET telegramId = ? WHERE id = ?", [chatId.toString(), user.id]);

        // Fetch their subscriptions to confirm
        const subs = await db.query(
          "SELECT s.name FROM strategies s JOIN subscriptions sub ON s.id = sub.strategyId WHERE sub.userId = ?",
          [user.id]
        );

        const stratList = subs.length > 0
          ? subs.map(s => `вЂў *${s.name}*`).join('\n')
          : '_No active strategies yet вЂ” purchase a plan on the dashboard._';

        bot.sendMessage(chatId,
          `вњ… *Linked successfully!*\n\n` +
          `Account: \`${user.email}\`\n\n` +
          `рџ“Љ *Your subscribed strategies:*\n${stratList}\n\n` +
          `You will now receive real-time BUY/SELL signals with entry price, take profit, and stop loss.\n\n` +
          `Send /help to see all commands.`,
          { parse_mode: 'Markdown' }
        );
        delete sessions[chatId];
      } catch (e) {
        bot.sendMessage(chatId, `вќЊ Login failed: ${e.message}\n\nSend /start to try again.`);
        delete sessions[chatId];
      }
    }
  });

} else {
  console.log('[Telegram] TELEGRAM_TOKEN missing or not configured. Bot disabled.');
}

// в”Ђв”Ђв”Ђ TelegramService class в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
class TelegramService {

  async connect(userId, telegramId) {
    if (!telegramId) throw new Error('telegramId is required');
    return await db.run("UPDATE users SET telegramId = ? WHERE id = ?", [telegramId.toString(), userId]);
  }

  async disconnect(userId) {
    return await db.run("UPDATE users SET telegramId = NULL WHERE id = ?", [userId]);
  }

  async sendDirectNotification(telegramId, message) {
    if (!bot || !telegramId) return;
    bot.sendMessage(telegramId, message, { parse_mode: 'Markdown' }).catch(console.error);
  }

  /**
   * Broadcast an ENTRY signal (buy/sell/long/short) to all users subscribed to this strategy.
   * Filters strictly: only users who have a subscription row for this strategyId receive it.
   */
  async broadcastSignal(signal) {
    if (!bot) return;

    const { strategyId, side } = signal;

    const strategy = await db.get("SELECT name FROM strategies WHERE id = ?", [strategyId]);
    if (!strategy) return;

    // Only subscribed users with a linked telegramId
    const users = await db.query(
      `SELECT DISTINCT u.telegramId
       FROM users u
       JOIN subscriptions s ON u.id = s.userId
       WHERE s.strategyId = ? AND u.telegramId IS NOT NULL`,
      [strategyId]
    );

    if (users.length === 0) return;

    const isEntry = ['buy', 'long', 'sell', 'short'].includes((side || '').toLowerCase());
    const message = isEntry
      ? formatEntryMessage(strategy.name, signal)
      : formatExitMessage(strategy.name, signal);

    console.log(`[Telegram] Broadcasting signal to ${users.length} user(s) for strategy ${strategy.name} (${side})`);

    for (const user of users) {
      bot.sendMessage(user.telegramId, message, { parse_mode: 'Markdown' }).catch(console.error);
    }
  }

  /**
   * Broadcast a CLOSE/EXIT signal when a trade is closed (TP hit / SL hit / manual close).
   * Called from strategyService signal tracker or paperService.
   */
  async broadcastClose(strategyId, symbol, side, price, pnl, status) {
    if (!bot) return;

    const strategy = await db.get("SELECT name FROM strategies WHERE id = ?", [strategyId]);
    if (!strategy) return;

    const users = await db.query(
      `SELECT DISTINCT u.telegramId
       FROM users u
       JOIN subscriptions s ON u.id = s.userId
       WHERE s.strategyId = ? AND u.telegramId IS NOT NULL`,
      [strategyId]
    );

    if (users.length === 0) return;

    const signal = { symbol, side, price, pnl, status };
    const message = formatExitMessage(strategy.name, signal);

    console.log(`[Telegram] Broadcasting close to ${users.length} user(s): ${symbol} ${status} PnL=${pnl?.toFixed(2)}%`);

    for (const user of users) {
      bot.sendMessage(user.telegramId, message, { parse_mode: 'Markdown' }).catch(console.error);
    }
  }

  // Legacy compat вЂ” kept for paperService calls
  async notifyTradeClosed(telegramId, strategyName, symbol, side, price, pnl) {
    if (!bot || !telegramId) return;
    const signal = { symbol, side, price, pnl, status: 'closed' };
    const message = formatExitMessage(strategyName, signal);
    bot.sendMessage(telegramId, message, { parse_mode: 'Markdown' }).catch(console.error);
  }
}

module.exports = new TelegramService();
